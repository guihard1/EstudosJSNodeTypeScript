console.log(1 === 1 && 2 >= 1); //Aqui ele está comparando se 1 é igual a 1 e se 2 é maior ou igual a 1.
console.log(1 === 1 && 1 >= 2); //Aqui ele está comparando se 1 é igual a 1 e se 1 é maior ou igual a 2.

console.log(1 === 2 || 1 === 1); //Aqui ele está comparando se 1 é igual a 2 ou se 1 é igual a 1.


/*
    && -> AND -> E -> Todas as expressões precisam ser verdadeiras para retornar true
    || -> OR -> OU -> Se uma das expressões for verdadeira, retorna true
    ! -> NOT -> NÃO -> Inverte o valor da expressão
*/