console.log(1 == 1);  //Ele compara o valor.
console.log(1 == "1"); //Tem que ser igual o valor, ele converte o tipo.
console.log(1 === "1"); //tem que ser exatamente igual, tanto o valor quanto o tipo.
console.log(1 === 1); //tem que ser exatamente igual, tanto o valor quanto o tipo.

console.log(1 != "1"); //Aqui ele está comparando se é diferente.
console.log(1 !== 1); //Aqui ele está comparando se é diferente.

console.log(3 > 2); //Aqui ele está comparando se é maior.
console.log(3 < 2); //Aqui ele está comparando se é menor.

log(3 >= 2); //Aqui ele está comparando se é maior ou igual.
console.log(1 <= 2); //Aqui ele está comparando se é menor ou igual.


